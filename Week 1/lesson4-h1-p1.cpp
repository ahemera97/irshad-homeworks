#include<iostream>
using namespace std;

int main()
{
	int x, y;
	cout << "Enter 2 numbers" << endl;
	cin >> x >> y;
	cout << x << "+" << y << "=" << x + y << endl;
	cout << x << "-" << y << "=" << x - y << endl;
	cout << x << "*" << y << "=" << x * y << endl;
	cout << x << "/" << y << "=" << x / y << endl;



	return 0;
}
